#include<iostream>
#include<limits>
using namespace std;
int main(){
   
    cout<<"maximum size of int is : "<<sizeof(int)<<" bytes"<<endl;
    cout<<"maximum size of float is : "<<sizeof(float)<<" bytes"<<endl;
    cout<<"maximum size of char is : "<<sizeof(char)<<" bytes"<<endl;
    cout<<"maximum size of double is : "<<sizeof(double)<<" bytes"<<endl;
    
    return 0;
}